char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	*dest = '\0';
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}
