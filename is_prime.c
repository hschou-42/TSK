char	is_prime(unsigned int n)
{
	unsigned int	m;

	m = 2;
	if (n == 0 || n == 1)
		return (0);
	while (m * m <= n)
	{
		if (n % m == 0)
			return (0);
		m++;
	}
	return (1);
}
