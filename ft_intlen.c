unsigned int	ft_intlen(int num)
{
	unsigned int	i;

	i = 1;
	if (num < 0)
	{
		i += 1;
		num *= -1;
	}
	while (10 <= num)
	{
		i += 1;
		num /= 10;
	}
	return (i);
}
