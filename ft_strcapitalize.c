char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i - 1] < '0' ||
				('9' < str[i - 1] && str[i - 1] < 'A') ||
				('Z' < str[i - 1] && str[i - 1] < 'a') ||
				'z' < str[i - 1]) &&
				('a' <= str[i] && str[i] <= 'z'))
			str[i] -= 32;
		else if ((('A' <= str[i - 1] && str[i - 1] <= 'Z') ||
				('a' <= str[i - 1] && str[i - 1] <= 'z')) &&
				('A' <= str[i] && str[i] <= 'Z'))
			str[i] += 32;
		i++;
	}
	return (str);
}
