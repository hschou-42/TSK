#include <unistd.h>

void	ft_putnbr_hex(int n)
{
	if (n == -2147483648)
	{
		write(1, "-80000000", 9);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (16 <= n)
		ft_putnbr_hex(n / 16);
	n = n % 16 + ((n % 16) < 10 ? '0' : 'a' - 10);
	write(1, &n, 1);
}

int		main(void)
{
	int	n;

	n = -2147483648;
	ft_putnbr_hex(n);
	return (0);
}
