int		ft_str_is_lowercase(char *str)
{
	int		i;
	short	low;

	i = 0;
	low = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'a' || 'z' < str[i])
		{
			low = 0;
			break ;
		}
		i++;
	}
	return (low);
}
