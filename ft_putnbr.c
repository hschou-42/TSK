#include <unistd.h>

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (10 <= n)
		ft_putnbr(n / 10);
	n = n % 10 + '0';
	write(1, &n, 1);
}
