int		ft_str_is_uppercase(char *str)
{
	int		i;
	short	upp;

	i = 0;
	upp = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || 'Z' < str[i])
		{
			upp = 0;
			break ;
		}
		i++;
	}
	return (upp);
}
