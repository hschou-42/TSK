#include <unistd.h>

void	ft_putnbr_bi(int n)
{
	if (n == -2147483648)
	{
		write(1, "-10000000000000000000000000000000", 33);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (2 <= n)
		ft_putnbr_bi(n / 2);
	n = n % 2 + '0';
	write(1, &n, 1);
}
